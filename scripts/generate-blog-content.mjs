// Compiles every content/blog/<slug>.md into a plain TypeScript module that
// exports the post bodies as string literals (the POST_BODIES map). Precompiling
// to a .ts module keeps content loading bundler-agnostic — it resolves the same
// way in the Next build and in Vitest, with no bundler-specific `.md` loader
// config to keep in sync. The .md files remain the authoring source of truth.
//
// Regenerated automatically via the pre{build,dev,test} npm hooks.
import { readFileSync, writeFileSync, readdirSync, mkdirSync } from "node:fs";
import { join, basename } from "node:path";

const root = process.cwd();
const contentDir = join(root, "content", "blog");
const outDir = join(root, "lib", "generated");
const outFile = join(outDir, "blog-content.ts");

const files = readdirSync(contentDir)
  .filter((f) => f.endsWith(".md"))
  .sort();

const entries = files.map((file) => {
  const slug = basename(file, ".md");
  const body = readFileSync(join(contentDir, file), "utf8");
  return `  ${JSON.stringify(slug)}: ${JSON.stringify(body)},`;
});

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outFile,
  `// AUTO-GENERATED by scripts/generate-blog-content.mjs — do not edit by hand.
// Source of truth: content/blog/*.md
export const POST_BODIES: Record<string, string> = {
${entries.join("\n")}
};
`
);

console.log(
  `generate-blog-content: wrote ${files.length} post${
    files.length === 1 ? "" : "s"
  } -> lib/generated/blog-content.ts`
);
